package pe.edu.universidad.entidades;

public class cliente {
	
	private int idClie;
	private String nomClie;
	private String apeClie;
	private String correo;
	private String direccion;
	private String dni;
	
	public int getIdClie() {
		return idClie;
	}
	public void setIdClie(int idClie) {
		this.idClie = idClie;
	}
	public String getNomClie() {
		return nomClie;
	}
	public void setNomClie(String nomClie) {
		this.nomClie = nomClie;
	}
	public String getApeClie() {
		return apeClie;
	}
	public void setApeClie(String apeClie) {
		this.apeClie = apeClie;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	
}
